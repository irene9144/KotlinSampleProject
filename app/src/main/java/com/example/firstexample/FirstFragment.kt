class FirstFragment : BaseFragment<FragmentFirstBinding>(FragmentFirstBinding::inflate) {
    // TODO: Rename and change types of parameters

    lateinit var viewModel: MainViewModel

    override fun onViewCreated(view: View, savedInstanceState: Bundle?) {
        super.onViewCreated(view, savedInstanceState)
        viewModel = ViewModelProvider(this).get(MainViewModel::class.java)
        viewModel.currentValue.observe(viewLifecycleOwner, Observer {
            viewBinding.numberTxtview.text = it.toString()
        })

        viewBinding.plusBtn.setOnClickListener { viewModel.increase() }
        viewBinding.minusBtn.setOnClickListener { viewModel.decrease() }
    }
